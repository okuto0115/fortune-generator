function _gen20(lead, core, close) {
  const out = [];
  for (let i = 0; i < 20; i++) {
    const a = lead[i % lead.length];
    const b = core[(i * 2) % core.length];
    const c = close[(i * 3) % close.length];
    out.push(`${a}${b}${c}`);
  }
  return out;
}

