export function buildTimeOptions(){
  // 開発者メモ：出生時間は“任意”。今は表示と将来拡張用に保持する
  const list = ["不明"];
  for (let h=0; h<24; h++){
    list.push(`${String(h).padStart(2,"0")}:00`);
    list.push(`${String(h).padStart(2,"0")}:30`);
  }
  return list;
}

